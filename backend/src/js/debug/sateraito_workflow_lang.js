    /**
     * getMsg
     *
     * メッセージ取得
     *
     * @param {String} aMsgCd メッセージコード
     * @return {String} 国際化メッセージ
     */
    getMsg: function (aMsgCd) {
      var locale = MyLang.getLocale();
      var text = '';

      if (locale == 'en' || locale == 'en-gb' || locale == 'en_GB') {
        text = MyLang.msgData_en_ALL[aMsgCd];
      } else if (locale == 'ja') {
        text = MyLang.msgData_ja_ALL[aMsgCd];
      } else {
        text = MyLang.msgData_ALL_ALL[aMsgCd];
      }

      if (typeof (text) == 'undefined') {
        text = aMsgCd;
      }

      return text
    },

